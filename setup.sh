# install packages
sudo apt install \
    curl \
    unzip \
    build-essential \
    zsh \
    git \
    neovim \
    tmux \
    stow \
    python3-dev \
    python3-pip \

# stow dotfiles
stow zsh
stow nvim
stow git
stow tmux

# add zsh as a login shell
command -v zsh | sudo tee -a /etc/shells

# use zsh as default shell
sudo chsh -s $(which zsh) $USER

